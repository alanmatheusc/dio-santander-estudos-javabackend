package edu.alan.estudos.poo.pilares;

public class Heranca {
    /*
    * Herança -> um pilar muito importante na POO, pois você consegue herdar metodos de outra classes que faça sentido você reutilizar
    * isso permite que você não repita código desnecessariamente. No java podemos utilizar o extends para herdar propriedades de outra classe.
    *
    * Você pode criar uma classe Pai generica, exemplo: Veiculo
    *
    * o Veiculo vai ter:
    * Chassi / get set
    * Rodas / get set
    * ... ...
    * .. ..
    * acelerar...
    *
    * e você apartir de Veiculo cria classes mais especificas, exemplo Moto
    *
    * public class Moto extends Veiculo{} -> moto vai ter chassi, rodas, ......
    * public class Carro extends Veiculo -> carro vai ter chassi, rodas, .....
    *
    * Então apartir de uma classe generica criamos classes mais especificas, mas é claro carro e moto tem maneiras de acelerar diferentes
    * por isso temos outros conceitos chamados  de ABSTRAÇÃO e POLIMORFISMO.
    * */
}
